def get_text(url):
    '''
    From each url obtain all text from header or p elements in the html file,
    and write to a string.
    '''
    
    connection = urllib2.urlopen(url)
    page = connection.read()
    soup = bs(page, 'html.parser')
    soup.prettify()
    text = ''

    # get title
    #title = soup.html.head.title.string
    #text += ' ' + title

    # get headers
    hs = ['h1', 'h2', 'h3', 'h4', 'h5', 'h6']
    for h in hs:
        for header in soup.findAll(h):
            text += ' ' + header.getText()

    # Main content
    for p in soup.findAll('p'):
        text += ' ' + p.getText()
 
    connection.close()

    return text

